    <h1 id="great-blue-heron-range-and-distribution-from-alaska-to-the-galapagos">Great Blue Heron Range and Distribution: From Alaska to the Galápagos</h1>
<p>The Great Blue Heron is one of the most widespread wading birds in the Americas. Its core range covers most of North America — from the southeast coast of Alaska across southern Canada and the entire continental United States — and extends south through Mexico and Central America to the northern coasts of Colombia and Venezuela, throughout the Caribbean, and out to the Galápagos Islands.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup> Remarkably, it is also a confirmed vagrant across the Atlantic: it has been recorded in the Azores and coastal Spain, and in 2008 a bird was admitted to the British List.<sup id="fnref2:2"><a class="footnote-ref" href="#fn:2">2</a></sup><sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup></p>
