<h2 id="regional-texture">Regional texture</h2>
<ul>
<li><strong>Pacific coast</strong>: The <em>fannini</em> subspecies occupies the coast from southern Alaska to Washington and does not migrate; British Columbia holds an estimated 5,000–6,000 birds, mostly in the Lower Mainland and southern province.<sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup><sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
<li><strong>Interior Canada</strong>: Breeding occurs along valley-bottom rivers such as the Kootenay, Columbia, Okanagan, and Thompson, with winter use restricted to ice-free stretches around places like Creston and Castlegar, BC.<sup id="fnref2:6"><a class="footnote-ref" href="#fn:6">6</a></sup></li>
<li><strong>Northeastern U.S.</strong>: Common around beaver ponds, reservoirs, and coastal wetlands; North Carolina's 2012 aerial survey alone tallied more than <strong>7,680 nests in 518 colonies</strong> in the Piedmont and Upper Coastal Plain.<sup id="fnref2:7"><a class="footnote-ref" href="#fn:7">7</a></sup></li>
<li><strong>Florida</strong>: Year-round and abundant; the only place to see all three plumage forms (blue, white "great white heron," and Würdemann's).<sup id="fnref:8"><a class="footnote-ref" href="#fn:8">8</a></sup></li>
<li><strong>California</strong>: Fairly common year-round in estuaries and fresh and saline wetlands; fewer rookeries in the south, many scattered across the north.<sup id="fnref:9"><a class="footnote-ref" href="#fn:9">9</a></sup></li>
</ul>
<h2 id="filling-the-map-a-generalists-strategy">Filling the map: a generalist's strategy</h2>
<p>Two traits explain the enormous range. First, dietary flexibility — a bird that can switch between fish, frogs, crabs, mice, and gophers is never far from a meal.<sup id="fnref:10"><a class="footnote-ref" href="#fn:10">10</a></sup> Second, hardiness: as long as open, fish-bearing water exists, herons can overwinter far north of where most people expect them, including frozen-edge rivers in Utah and New England.<sup id="fnref:11"><a class="footnote-ref" href="#fn:11">11</a></sup><sup id="fnref:12"><a class="footnote-ref" href="#fn:12">12</a></sup> The northern boundary of year-round distribution is drawn not by cold but by <strong>ice</strong>: when water freezes solid and snow blankets the uplands, herons must move.<sup id="fnref2:11"><a class="footnote-ref" href="#fn:11">11</a></sup></p>
<h2 id="range-dynamics">Range dynamics</h2>
<p>The footprint is not static. Breeding Bird Survey data show the species increased in the U.S. between 1966 and 2019, and Audubon's Survival by Degrees climate modeling rates the Great Blue Heron "Stable" under warming — projecting about 36% range gained, 100% of current range maintained, and 0% lost at +1.5 °C.<sup id="fnref:13"><a class="footnote-ref" href="#fn:13">13</a></sup><sup id="fnref:14"><a class="footnote-ref" href="#fn:14">14</a></sup> Colonization of created habitats — reservoirs, beaver ponds, aquaculture districts, urban parks — has driven much of the 20th-century expansion, as in North Carolina's interior and British Columbia's interior valleys.<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup><sup id="fnref:7"><a class="footnote-ref" href="#fn:7">7</a></sup></p>
