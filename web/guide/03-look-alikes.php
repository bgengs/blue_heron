<h2 id="great-blue-heron-vs-other-herons">Great Blue Heron vs. other herons</h2>
<ul>
<li><strong>Little Blue Heron</strong> — much smaller (56–74 cm long, under 0.5 kg), with a two-toned blue-gray-based bill and no white on the head; juveniles are all-white and can be confused with egrets.<sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
<li><strong>Reddish Egret &amp; Tricolored Heron</strong> — far smaller, coastal, with distinct proportions and colors; both lack the Great Blue's white-headed, black-striped pattern.<sup id="fnref2:3"><a class="footnote-ref" href="#fn:3">3</a></sup></li>
<li><strong>Grey Heron</strong> (<em>Ardea cinerea</em>) — the Great Blue's Old World ecological twin; a rare vagrant to North America's northeast coasts. It is somewhat smaller with a uniformly pale gray neck, lacking the Great Blue's rusty tones.<sup id="fnref3:3"><a class="footnote-ref" href="#fn:3">3</a></sup></li>
<li><strong>Cocoi Heron</strong> — overlaps in northern South America; distinguished by a striking white neck and solid black crown (dull juveniles are the tricky ones).<sup id="fnref4:3"><a class="footnote-ref" href="#fn:3">3</a></sup></li>
<li><strong>Sandhill Crane, Wood Stork, American White Pelican</strong> — all large and long-legged, but none combines the folded-neck flight, shaggy plumes, and dagger bill of a Great Blue.</li>
</ul>
<h2 id="the-trickiest-case-wurdemanns-and-the-great-white-heron">The trickiest case: Würdemann's and the "Great White Heron"</h2>
<p>Within south Florida and the Keys, Great Blue Herons occur in three plumage types: the standard blue-gray form; the entirely white <strong>great white heron</strong> (subspecies <em>occidentalis</em>); and the intermediate <strong>Würdemann's heron</strong> — a grayish body with a mostly white head and neck.<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup><sup id="fnref:7"><a class="footnote-ref" href="#fn:7">7</a></sup> Birders visiting Florida should learn all three; the white birds are routinely misreported as Great Egrets, and Würdemann's birds baffle even experienced observers. For the full taxonomic story, see <a href="04-taxonomy-subspecies.md">Taxonomy &amp; Subspecies</a>.</p>
