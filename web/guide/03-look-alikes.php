    <h1 id="great-blue-heron-look-alikes-heron-vs-egret-vs-crane-and-more">Great Blue Heron Look-alikes: Heron vs. Egret vs. Crane and More</h1>
<p>The Great Blue Heron's size and shape make it nearly unmistakable across most of North America, but a handful of species — and one famous color morph — regularly cause confusion. This page collects the comparisons people search for most.</p>
