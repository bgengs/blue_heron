<h2 id="great-blue-heron-vs-great-egret">Great Blue Heron vs. Great Egret</h2>
<p>This is the classic confusion, especially in Florida where the all-white "great white heron" morph lives. The Great Egret is snow-white, noticeably smaller and more slender, and — the clincher — has <strong>black legs and feet</strong>; the white-morph Great Blue Heron is larger and heavier-billed with <strong>yellowish or greenish legs</strong>.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup> The Great Blue stands taller and is roughly twice as heavy as a Great Egret.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup> In flight both fold their necks, but the Great Blue's bulkier frame and slower, more labored wingbeats stand out.</p>
<h2 id="great-blue-heron-vs-sandhill-crane">Great Blue Heron vs. Sandhill Crane</h2>
<p>Herons and cranes aren't close relatives, but their silhouettes invite mix-ups. In flight the difference is absolute: <strong>herons fly with the neck folded into an "S"; cranes fly with the neck extended straight out</strong>.<sup id="fnref2:2"><a class="footnote-ref" href="#fn:2">2</a></sup> Other distinctions: Sandhill Cranes have a crimson crown patch, shorter bills, only three toes (they can't perch in trees), eat mostly tubers and vegetation rather than fish, gather in flocks of thousands, and yodel loudly in flight — while herons are near-silent and usually travel alone or in small groups.<sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup></p>
