    <h1 id="remarkable-adaptations-of-the-great-blue-heron">Remarkable Adaptations of the Great Blue Heron</h1>
<p>Four-plus feet tall, five pounds, armed like a spearfish and groomed like a cat — the Great Blue Heron is a toolkit of specialized anatomy. These are the adaptations that make the bird work.</p>
<h2 id="the-spring-loaded-neck">The spring-loaded neck</h2>
<p>Herons have specially shaped neck vertebrae that let the neck coil into a tight "S." The fold serves two purposes: aerodynamics in flight (the head rides above the shoulders instead of dragging a long neck through the air) and weaponry — uncoiling the neck drives the bill forward with explosive force and reach, so prey never sees the strike coming.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup> One naturalist describes the strike as approaching 90 mph; whatever the true figure, it is effectively instantaneous to the human eye.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup></p>
<h2 id="powder-down-built-in-soap">Powder down: built-in soap</h2>
<p>Great Blue Herons stay clean in slimy water using <strong>powder down</strong> — specialized chest feathers that grow continuously and fray into a fine dust. The heron crumbles the feathers and combs the powder through its plumage like a washcloth, absorbing fish slime, oils, and silt, which it then scratches out.<sup id="fnref2:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup> The tool for the job is a <strong>pectinate claw</strong> — a fringed, comb-like nail on the middle toe.<sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup> This cleaning system is one reason rain beads off incubating parents so effectively.<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup></p>
<h2 id="night-vision-eyes">Night-vision eyes</h2>
<p>A high percentage of rod-type photoreceptors gives Great Blue Herons excellent night vision, allowing them to hunt in darkness as well as daylight — unusual among members of their genus and a major edge when fish move shallow at night.<sup id="fnref2:4"><a class="footnote-ref" href="#fn:4">4</a></sup><sup id="fnref:7"><a class="footnote-ref" href="#fn:7">7</a></sup></p>
<h2 id="hollow-boned-giant">Hollow-boned giant</h2>
<p>Despite standing up to four feet tall with a wingspan approaching seven feet, the bird weighs only about five to six pounds — hollow bones (shared by all birds) keep the frame light enough for slow, economical flight and high-altitude migratory legs.<sup id="fnref3:4"><a class="footnote-ref" href="#fn:4">4</a></sup><sup id="fnref:8"><a class="footnote-ref" href="#fn:8">8</a></sup></p>
<h2 id="feet-for-water-and-trees">Feet for water and trees</h2>
<p>Four toes — three forward, one back — give herons a stable base on mudflats and the grip needed to perch and nest in trees (cranes, with three toes, cannot perch).<sup id="fnref2:3"><a class="footnote-ref" href="#fn:3">3</a></sup> Long toes spread the bird's weight over soft mud and aquatic vegetation.</p>
