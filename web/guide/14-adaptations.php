<h2 id="behavior-as-adaptation">Behavior as adaptation</h2>
<p>Adaptation isn't only anatomy. Flexible diet switching (fish to rodents when wetlands freeze), winter scavenging from diving ducks, colonial nesting in predator-inaccessible sites, and seasonal monogamy that lets pairs re-form optimally each year are all behavioral traits that widen the ecological envelope of the species — and explain its continent-spanning success.<sup id="fnref:11"><a class="footnote-ref" href="#fn:11">11</a></sup><sup id="fnref:12"><a class="footnote-ref" href="#fn:12">12</a></sup></p>
