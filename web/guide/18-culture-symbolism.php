<h2 id="the-civic-heron">The civic heron</h2>
<p>The Great Blue Heron's modern symbolic home is Seattle, which on <strong>March 17, 2003</strong> made it the official city bird — a fitting emblem for a city of gray-blue weather, shorelines, and seafood.<sup id="fnref:8"><a class="footnote-ref" href="#fn:8">8</a></sup><sup id="fnref:9"><a class="footnote-ref" href="#fn:9">9</a></sup> The designation, pushed by local birders and later championed by groups like Heron Habitat Helpers, turned the bird into an ambassador for urban wetland protection, with the city's largest colony nesting in view of the Ballard Locks.<sup id="fnref:10"><a class="footnote-ref" href="#fn:10">10</a></sup></p>
<h2 id="in-art-and-popular-imagination">In art and popular imagination</h2>
<p>John James Audubon immortalized the species in Plate 211 of <em>The Birds of America</em>; today the bird's silhouette — neck folded, legs trailing — is a staple of wetland logos, conservation branding, and photography. Its pterodactyl-like outline in flight makes it a favorite "dinosaur encounter" for children, and nest cams have turned individual herons into minor celebrities watched by millions.<sup id="fnref:11"><a class="footnote-ref" href="#fn:11">11</a></sup><sup id="fnref:12"><a class="footnote-ref" href="#fn:12">12</a></sup></p>
