    <h1 id="the-great-blue-heron-in-culture-myth-and-symbolism">The Great Blue Heron in Culture, Myth, and Symbolism</h1>
<h2 id="patience-made-flesh">Patience made flesh</h2>
<p>Across cultures, the heron's meaning grows from its hunting style: stillness, vigilance, and a single decisive strike. It is nearly impossible to watch a heron fish without thinking about patience — a lesson naturalists and writers have drawn for centuries.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup></p>
<h2 id="indigenous-traditions-of-north-america">Indigenous traditions of North America</h2>
<ul>
<li><strong>Good luck and good fishing</strong>: in many Native American traditions, herons symbolize patience and good fortune; on the Northwest Coast, a fisherman who sees a heron takes it as a sign of a successful trip.<sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup></li>
<li><strong>Clan animal</strong>: the Iroquois nations and the Menominee (whose Great Blue Heron Clan totem is called <em>Saqsakaew</em>) count Heron among their clan animals.<sup id="fnref2:2"><a class="footnote-ref" href="#fn:2">2</a></sup></li>
<li><strong>The Heron and the Hummingbird</strong>: a Hitchiti tale tells how Heron and Hummingbird raced four days for ownership of all the fish in the water. Hummingbird darted ahead but slept each night; Heron flew slowly, steadily, and without stopping — and won. That, the story says, is why hummingbirds sip flowers while herons eat fish.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup></li>
<li><strong>Wisdom and judgment</strong>: in Native American tradition more broadly the heron embodies wisdom and good judgment; the Iroquois considered a heron sighting before a hunt a very good omen.<sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup></li>
<li><strong>A heron-named place</strong>: the kʷikʷəƛ̓əm (Kwikwetlem) First Nation renamed the Riverview lands in British Columbia <em>səmiq̓wəʔelə</em> — "The Place of the Great Blue Heron" in the hən̓q̓əmin̓əm language. Nuu-chah-nulth legends credit the heron with first teaching people to stand tall with self-confidence.<sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
</ul>
