<p>Any local eBird hotspot with wetland habitat will do — herons are among the most reported birds on the continent.</p>
<h2 id="watching-the-breeding-season-responsibly">Watching the breeding season responsibly</h2>
<p>Heronries are spectacular but fragile. Follow these rules and you'll be welcome back:</p>
<ol>
<li><strong>Keep 100 m (330 ft) or more</strong> from active nests; use binoculars or a long lens instead of approaching.<sup id="fnref:14"><a class="footnote-ref" href="#fn:14">14</a></sup></li>
<li><strong>Never use drones</strong> near colonies (banned at Venice Rookery and broadly harmful).<sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
<li><strong>Keep noise down</strong> — loud or unusual sounds cause abandonment, especially before eggs hatch.<sup id="fnref2:14"><a class="footnote-ref" href="#fn:14">14</a></sup></li>
<li><strong>Leave pets at home</strong>; stay on marked trails and respect fences (they protect birds <em>and</em> people from falling sticks and guano).<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup></li>
<li>Visit in the <strong>early morning</strong> for the most activity and best light.<sup id="fnref2:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
</ol>
<h2 id="photography-notes">Photography notes</h2>
<p>The Venice Rookery has become an international photography mecca precisely because nests sit near eye level across a narrow pond — modest lenses work.<sup id="fnref:15"><a class="footnote-ref" href="#fn:15">15</a></sup> Everywhere else, herons reward patience differently: a motionless hunting bird lets you compose carefully, and the strike — if you're ready — is the shot of the day. Flight shots are easiest at colony flight lines at dawn and dusk, when birds commute between rookery and feeding grounds.<sup id="fnref2:4"><a class="footnote-ref" href="#fn:4">4</a></sup></p>
