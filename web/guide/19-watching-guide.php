    <h1 id="watching-great-blue-herons-where-when-and-how">Watching Great Blue Herons: Where, When, and How</h1>
<h2 id="the-good-news-theyre-findable-almost-everywhere">The good news: they're findable almost everywhere</h2>
<p>Great Blue Herons live year-round across most of the United States and southern Canada anywhere there is fish-bearing water — shorelines, riverbanks, marsh and pond edges, estuaries, even drainage ditches and golf-course ponds.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup> Scan shallow water for a tall, statue-still gray figure; look up for the slow-winged, neck-tucked silhouette.<sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup> Dawn and dusk are peak activity, and because hunting herons stand in the open and stay still, they are among the easiest large birds to observe and photograph.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup><sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup></p>
