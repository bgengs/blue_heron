    <h1 id="great-blue-heron-sounds-and-calls-a-guide-to-the-frawnk">Great Blue Heron Sounds and Calls: A Guide to the "Frawnk"</h1>
<p>Great Blue Herons are mostly silent birds — stealth is their hunting strategy — but when they do vocalize, the sound is unmistakable: a harsh, hoarse, almost prehistoric squawk that has convinced more than a few listeners they've heard a dinosaur.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup> The voice most people know is the alarm squawk given when a heron is flushed from a shoreline; at breeding colonies, though, the birds are remarkably chatty, with a documented vocabulary of <strong>seven distinct calls</strong>.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup></p>
<h2 id="the-seven-call-vocal-array">The seven-call vocal array</h2>
<table>
<thead>
<tr>
<th>Call</th>
<th>What it sounds like</th>
<th>When it's used</th>
</tr>
</thead>
<tbody>
<tr>
<td><strong>Frawnk</strong></td>
<td>A rapid, drawn-out squawk averaging ~20 seconds</td>
<td>Alarm and aggression toward other herons, day or night; the source of the New England nickname "Crank"<sup id="fnref3:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Go-go-go</strong></td>
<td>A series of clucks</td>
<td>First response to a slowly building disturbance, at feeding sites and colonies; escalates to <em>Frawnk</em> if the threat continues<sup id="fnref4:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Awk</strong></td>
<td>A startling scream, ~2.3 seconds</td>
<td>Extreme disturbance, e.g., a predator attack<sup id="fnref5:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Gooo</strong></td>
<td>Resembles the bleat of a calf</td>
<td>Ends the "Full Forward" courtship display<sup id="fnref6:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Ee</strong></td>
<td>A two-segment call</td>
<td>Various social situations, mostly in flight, day and night<sup id="fnref7:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Roh-roh-roh</strong></td>
<td>A ~3.6-second series of squawks</td>
<td>Advertises ownership of a feeding territory; also given arriving at the nest — likely functions in mate recognition<sup id="fnref8:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
<tr>
<td><strong>Landing call</strong></td>
<td>Similar to <em>Roh-roh-roh</em></td>
<td>Given when arriving at the nest; mate recognition<sup id="fnref9:3"><a class="footnote-ref" href="#fn:3">3</a></sup></td>
</tr>
</tbody>
</table>
<p>Descriptions follow Birds of the World (Vennesland &amp; Butler 2020), drawing on R. D. Bayer's colony studies. Calls intergrade and vary considerably between individuals.<sup id="fnref2:3"><a class="footnote-ref" href="#fn:3">3</a></sup></p>
<h2 id="how-to-hear-them">How to hear them</h2>
<p>The most reliable places to hear Great Blue Herons:</p>
<ol>
<li><strong>Flush one accidentally</strong> (you will): the departing <em>frawnk</em> is the everyday heron sound of North American waterways.<sup id="fnref2:2"><a class="footnote-ref" href="#fn:2">2</a></sup></li>
<li><strong>Visit a heronry in breeding season</strong>: colonies are loud with greeting squawks, bill-clattering, and the "che-che-che" of chicks (which deepens to "grr-grr-grr" as they grow).<sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup><sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup></li>
<li><strong>Online</strong>: the Cornell Lab's Macaulay Library hosts extensive recordings, and the All About Birds sounds page features the go-go-go building into a full frawnk.<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup></li>
</ol>
<h2 id="beyond-the-voice">Beyond the voice</h2>
<p>Herons also communicate mechanically: both sexes <strong>snap their bill tips together</strong> ("bill-clappering") during territorial and pair-bond displays, a sound that carries well across a colony.<sup id="fnref:7"><a class="footnote-ref" href="#fn:7">7</a></sup> Chicks add their own soundtrack — a loud clacking when begging for food, audible in the nest-cam era to millions of viewers.<sup id="fnref:8"><a class="footnote-ref" href="#fn:8">8</a></sup></p>
