<h2 id="how-they-hunt">How they hunt</h2>
<p>The method is patience perfected. A hunting heron wades with slow, deliberate steps or stands absolutely motionless — often in water less than a foot deep, sometimes belly-deep — peering into the water until prey comes within range, then straightening its S-coiled neck in a strike so fast it registers as instantaneous to the human eye.<sup id="fnref2:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:5"><a class="footnote-ref" href="#fn:5">5</a></sup> Small prey is grabbed in the mandibles; large fish may be <strong>impaled</strong> on the bill, then shaken to break or relax the sharp spines before being swallowed whole, head-first.<sup id="fnref:6"><a class="footnote-ref" href="#fn:6">6</a></sup> Specialized neck vertebrae make the coiled, spring-loaded strike possible, and a high density of rod photoreceptors lets herons hunt at night as effectively as by day — one of the few members of their genus to do so.<sup id="fnref:7"><a class="footnote-ref" href="#fn:7">7</a></sup><sup id="fnref:8"><a class="footnote-ref" href="#fn:8">8</a></sup></p>
<p>Hunting is not limited to water. Herons stalk voles and gophers through fields, snatch rails at marsh edges, perch on floating kelp beds waiting for fish, and stand at commercial fish operations where scraps accumulate.<sup id="fnref3:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref2:8"><a class="footnote-ref" href="#fn:8">8</a></sup> They may hunt from floating objects and shoreline perches as well.<sup id="fnref:9"><a class="footnote-ref" href="#fn:9">9</a></sup></p>
<h2 id="how-much-do-they-eat">How much do they eat?</h2>
<p>An adult consumes roughly <strong>13 ounces of fish per day</strong> — about three 6-inch koi or ten 2-inch goldfish — and can double that intake when feeding chicks.<sup id="fnref:10"><a class="footnote-ref" href="#fn:10">10</a></sup> A breeding pair with two chicks burns about 2,000 calories a day as a family, which keeps both parents foraging almost constantly; chicks gain nearly 40 times their hatch weight (about 50 g to about 2 kg) in eight weeks.<sup id="fnref:11"><a class="footnote-ref" href="#fn:11">11</a></sup></p>
<h2 id="risks-of-gluttony">Risks of gluttony</h2>
<p>Their ambition occasionally outruns their anatomy. Herons swallow prey whole and can manage surprisingly wide items, but there are documented cases of birds <strong>choking to death on oversized prey</strong> — including deaths caused by attempting to swallow Pacific lamprey.<sup id="fnref:12"><a class="footnote-ref" href="#fn:12">12</a></sup><sup id="fnref2:3"><a class="footnote-ref" href="#fn:3">3</a></sup></p>
