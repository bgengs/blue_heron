<p>Regional studies show how completely the diet tracks local abundance: in <strong>Nova Scotia, 98% of the diet was flounder</strong>; California herons take roughly 75% fish (mostly species not sought by anglers); British Columbia birds key on sticklebacks, gunnels, sculpins, and perch; and in one <strong>Idaho study, voles made up 24–40% of the diet</strong>, with muskrat and weasel remains in pellets.<sup id="fnref:3"><a class="footnote-ref" href="#fn:3">3</a></sup><sup id="fnref:4"><a class="footnote-ref" href="#fn:4">4</a></sup></p>
