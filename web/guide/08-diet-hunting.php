    <h1 id="what-do-great-blue-herons-eat-diet-and-hunting-strategy">What Do Great Blue Herons Eat? Diet and Hunting Strategy</h1>
<p>Short answer: nearly anything within striking distance. The Great Blue Heron is an opportunistic apex predator of wetlands — fish are the mainstay, but the full menu runs to amphibians, reptiles, crustaceans, insects, small mammals, and other birds.<sup id="fnref:1"><a class="footnote-ref" href="#fn:1">1</a></sup><sup id="fnref:2"><a class="footnote-ref" href="#fn:2">2</a></sup></p>
